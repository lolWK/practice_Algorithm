// 두 수의 합
function solution(num1, num2) {
  var answer = num1 + num2;
  return answer;
}

// 두 수의 차
function solution(num1, num2) {
  var answer = num1 - num2;
  return answer;
}

// 두 수의 곱
function solution(num1, num2) {
  var answer = 0;
  answer = num1 * num2;
  return answer;
}

// 몫 구하기
function solution(num1, num2) {
  var answer = Math.floor(num1 / num2);
  return answer;
}

function solution(num1, num2) {
  return parseInt(num1 / num2);
}

// 두 수의 나눗셈
function solution(num1, num2) {
  var answer = parseInt((num1 / num2) * 1000);
  return answer;
}
